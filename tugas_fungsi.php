<h2><b>TUGAS dan FUNGSI </b></h2><br>

<br><p  align="justify">Dalam melaksanakan tugasnya, Dinas Tenaga Kerja, Perindustrian, Koperasi dan UKM menyelenggarakan fungsi:</p>
<p align="justify"><font face="Times New Roman">
1.	Perumusan kebijakan daerah di bidang penempatan, pelatihan dan produktifitas tenaga kerja, hubungan industrial dan perselisihan ketenagakerjaan, perindustrian, koperasi, usaha kecil dan menengah;<br>

2.	Penetapan kebijakan teknis di bidang penempatan, pelatihan dan produktifitas tenaga kerja, hubungan industrial dan perselisihan ketenagakerjaan, perindustrian, koperasi, usaha kecil dan menengah;<br>

3.	Pengkoordinasikan perumusan program dan kegiatan di bidang penempatan, pelatihan dan produktifitas tenaga kerja, hubungan industrial dan perselisihan ketenagakerjaan, perindustrian, koperasi, usaha kecil dan menengah;<br>

4.	Penyelanggaraan kebijakan, program dan kegiatan di bidang penempatan, pelatihan dan produktifitas tenaga kerja, hubungan industrial dan perselisihan ketenagakerjaan, perindustrian, koperasi, usaha kecil dan menengah;<br>

5. Pengendalian dan pelaporan di bidang penempatan, pelatihan dan produktifitas tenaga kerja, hubungan industrial dan perselisihan ketenagakerjaan, perindustrian, koperasi, usaha kecil dan menengah;<br>

6.	Penyelenggaraan administrasi dinas penempatan, pelatihan dan produktifitas tenaga kerja, hubungan industrial dan perselisihan ketenagakerjaan, perindustrian, koperasi, usaha kecil dan menengah;<br>

7.	Penyelenggaraan fungsi kesekretariatan dinas;<br>

8.	Pengendalian penyelenggaraan tugas Unit Pelaksana Teknis Dinas; dan<br>

9.	Pelaksanaan tugas dan fugsi kedinasan lain yang diberikan oleh Bupati berdasarkan peraturan perundang-undangan yang berlaku.<br></p></font>